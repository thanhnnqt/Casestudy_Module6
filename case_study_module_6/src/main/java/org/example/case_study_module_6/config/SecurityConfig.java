package org.example.case_study_module_6.config;

import org.example.case_study_module_6.security.JwtAuthenticationFilter;
import org.springframework.context.annotation.Bean;
import org.springframework.context.annotation.Configuration;
import org.springframework.http.HttpMethod;
import org.springframework.security.config.annotation.web.builders.HttpSecurity;
import org.springframework.security.crypto.bcrypt.BCryptPasswordEncoder;
import org.springframework.security.crypto.password.PasswordEncoder;
import org.springframework.security.web.SecurityFilterChain;
import org.springframework.security.web.authentication.UsernamePasswordAuthenticationFilter;
import org.springframework.web.cors.CorsConfiguration;
import org.springframework.web.cors.CorsConfigurationSource;
import org.springframework.web.cors.UrlBasedCorsConfigurationSource;
import java.util.Arrays;
import java.util.List;

@Configuration
public class SecurityConfig {

    private final JwtAuthenticationFilter jwtFilter;

    public SecurityConfig(JwtAuthenticationFilter jwtFilter) {
        this.jwtFilter = jwtFilter;
    }

    @Bean
    public SecurityFilterChain filterChain(HttpSecurity http) throws Exception {
        http
                .csrf(csrf -> csrf.disable())
                .cors(cors -> cors.configurationSource(corsConfigurationSource()))
                .authorizeHttpRequests(auth -> auth

                        // 1. PREFLIGHT
                        .requestMatchers(HttpMethod.OPTIONS, "/**").permitAll()

                        // 2. AUTH - PUBLIC
                        .requestMatchers(
                                "/auth/login",
                                "/auth/register",
                                "/auth/google",
                                "/auth/verify-email",
                                "/auth/forgot-password",
                                "/auth/reset-password",
                                "/auth/me"
                        ).permitAll()

                        // 3. AUTH - REQUIRE LOGIN
                        .requestMatchers("/auth/change-password")
                        .hasAnyRole("CUSTOMER", "EMPLOYEE", "ADMIN")

                        // 4. CUSTOMER
                        .requestMatchers("/api/customers/me")
                        .hasAnyRole("CUSTOMER", "EMPLOYEE", "ADMIN")

                        // 5. PUBLIC APIs
                        .requestMatchers(
                                "/swagger-ui/**",
                                "/v3/api-docs/**",
                                "/ws-chat/**", "/app/**", "/topic/**", "/queue/**", "/user/**",
                                "/api/payment/**",
                                "/api/bookings/online",
                                "/api/airports/**",
                                "/api/master/airports",
                                "/api/master/airlines",
                                "/api/master/routes",
                                "/api/news/**"
                        ).permitAll()

                        // 6. SEARCH
                        .requestMatchers(HttpMethod.GET, "/api/flights/**").permitAll()

                        // 7. ROLE BASED
                        .requestMatchers("/api/bookings/my-history")
                        .hasAnyRole("CUSTOMER", "EMPLOYEE", "ADMIN")

                        .requestMatchers("/api/bookings/**").hasAnyRole("EMPLOYEE", "ADMIN")
                        .requestMatchers("/api/flights/**").hasAnyRole( "ADMIN")
                        .requestMatchers("/api/customers/**").hasAnyRole("EMPLOYEE", "ADMIN")
                        .requestMatchers("/api/master/**").hasAnyRole("EMPLOYEE", "ADMIN", "CUSTOMER")
//                        .requestMatchers("/v1/api/employees/**").hasRole("ADMIN")
                        .requestMatchers("/v1/api/employees/**").hasAnyRole("ADMIN", "EMPLOYEE")
                        .requestMatchers("/v1/api/employees/**").hasAnyRole("EMPLOYEE", "ADMIN")
                        .requestMatchers("/api/reports/**").hasRole("ADMIN")
                        .requestMatchers("/api/admin/news/**").hasRole("ADMIN")

                        // 8. DEFAULT
                        .anyRequest().authenticated()
                )
                .addFilterBefore(jwtFilter, UsernamePasswordAuthenticationFilter.class);

        return http.build();
    }

    @Bean
    public CorsConfigurationSource corsConfigurationSource() {
        CorsConfiguration config = new CorsConfiguration();
        config.setAllowedOriginPatterns(List.of("*"));
        config.setAllowedMethods(Arrays.asList("GET", "POST", "PUT", "DELETE", "PATCH", "OPTIONS"));
        config.setAllowedHeaders(List.of("*"));
        config.setAllowCredentials(true);

        UrlBasedCorsConfigurationSource source = new UrlBasedCorsConfigurationSource();
        source.registerCorsConfiguration("/**", config);
        return source;
    }

    @Bean
    public PasswordEncoder passwordEncoder() {
        return new BCryptPasswordEncoder();
    }
}